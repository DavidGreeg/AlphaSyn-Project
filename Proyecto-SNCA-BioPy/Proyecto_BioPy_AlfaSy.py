# A pesar de  haberse descubierto  aproximadamente hace poco más de dos 
# décadas (1997) y tener una relación muy estrecha con la enfermedad de
# Parkinson pues un gran factor para el desarrollo de este es su acumu-
# lación dentro de las neuronas, dentro de los Cuerpos de Lewis, la ASN
# o alfa-synucleína, es  una proteina implicada  en  múltiples procesos 
# dentro de las células del  sistema  nervioso  central, y abundante en
# neuronas.  Sin embargo, muchas de sus interacciones con otras proteí-
# nas, así como su propia  regulación y función  precisas siguen siendo
# temas poco comprendidas o directamente desconocidas.

# Alfa-Synucleína es una proteína dinámica, pues adopta diferentes for-
# mas para (presumiblemente) diferentes funciones.  Estas formas pueden
# interactuar, combinarse y formar cadenas sencillas de alfa-synucleína
# Dichas cadenas son transitorias (se forman y se descomponen de manera
# continua). La  longitud y tiempo  de vida de las mismas está regulada
# por las proteínas chaperonas que las identifiquen y proteasas que las
# degraden. No obstante, desbalances en esta regulación como:
#			-Las cadenas sencillas son demasiado abundantes o largas
#			-Reducción en el número o eficiencia de proteasas
#			-Reducción en el número o eficiencia de chaperonas

# Como consecuencia las cadenas sencillas (o hebras) de α-syn (abrevia-
# ción para alfa-synucleína) se acumula en la célula, de manera que pro
# -gresivamente se combinan formando fibras. Las fibras a su vez forman
# agregados, que ocupan un gran espacio en las neuronas, y posteriormen
# -te se pueden desarrollar en Cuerpos de Lewis.

# Dado que la estructura 3D (es decir, el plegamiento) que la α-syn to-
# me influye mucho en la patogenicidad de la misma, así como su capaci-
# dad de formar fibras, y afectar el plegamiento de otras alfa-synucle- 
# ínas, el estudio de sus modificaciones post-traduccionales es un cam-
# po muy informativo en el área de la Fisiología del Parkinson.

# Conociendo ésto, el presente  proyecto se enfocará en identificar los
# dominios se la α-syn asociados al plegamiento normal de la proteína y
# los que estan asociados a su  estado fibril.  Si  lo resumiera en una 
# pregunta, esta sería:  ¿Qué  modificaciones (y en que regiones) están 
# asociadas al estado fibril y al estado de cadena sencilla de la α-syn?

